// Deep structure type that preserves shape but allows any string values
type DeepStringify<T> = {
  [K in keyof T]: T[K] extends string
    ? string
    : T[K] extends readonly string[]
      ? readonly string[] | string[]
      : T[K] extends object
        ? DeepStringify<T[K]>
        : T[K];
};

